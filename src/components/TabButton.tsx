import React from "react";

interface TabButtonProps {
  id: string;
  label: string;
  active: boolean;
  onClick: (id: string) => void;
  className?: string;
  iconOnly?: boolean;
  customIcon?: React.ReactNode;
}

export function TabButton({
  id,
  label,
  active,
  onClick,
  className = "",
  iconOnly = false,
  customIcon,
}: TabButtonProps) {
  const baseClasses = "group relative flex items-center gap-3 rounded-lg border border-transparent bg-transparent text-sm font-medium transition-all duration-150 focus:outline-none w-full overflow-hidden";
  
  const layoutClasses = iconOnly
    ? "flex-col justify-center h-14 p-0 text-[12px] font-semibold uppercase tracking-wider"
    : "px-3 py-2.5 text-left";

  const stateClasses = active
    ? "text-text-primary font-semibold bg-[var(--accent-soft)] border-l-[3px] border-l-[var(--accent)]"
    : "text-text-secondary hover:text-text-primary bg-transparent border-l-[3px] border-l-transparent";

  return (
    <button
      className={`${baseClasses} ${layoutClasses} ${stateClasses} ${className}`.trim()}
      onClick={() => onClick(id)}
      aria-current={active ? "page" : undefined}
      aria-label={iconOnly ? label : undefined}
      title={label}
    >
      <span 
        className={`grid place-items-center w-5 h-5 transition-colors duration-150 ${active ? 'text-[var(--accent)]' : ''}`}
        aria-hidden="true"
      >
        {customIcon || <DefaultIcon id={id} />}
      </span>
      {!iconOnly && (
        <span className="truncate">
          {label}
        </span>
      )}
    </button>
  );
}

